package cursoJava2024;

public class ExercícioArraysemJava {

	public static void main(String[] args) {
		// Declaração e inicialização do array de inteiros com 5 elementos
		int[] numeros = { 10, 20, 30, 40, 50};
		// Usando um laço for-each para percorrer o array e imprimir cada valor
		System.out.println("Valores do array usando é");
		for(int numero : numeros) {
			System.out.println("Valor "+ numero);
		}

	}

}
